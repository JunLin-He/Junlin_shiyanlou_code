"""
Bars Module
=============
This is a demo module that use to print different split line
"""
def starbar(num):
	"""print * split line

	: arg num: the length of line
	"""
	print('*' * num)

def hashbar(num):
	"""print # split line

	: arg num: the length of line
	"""
	print('#' * num)

def simplebar(num):
	"""print - split line

	: arg num: the length of line
	"""
	print('-' * num)